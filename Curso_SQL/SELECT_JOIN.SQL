INNER JOIN NOTAS_FISCAIS
ON A.MATRICULA = B.MATRICULA;

SELECT A.NOME, A.BAIRRO, B.CPF, B.DATA_VENDA, B.NUMERO, 
COUNT(*) FROM tabela_de_vendedores A 
INNER JOIN notas_fiscais B
ON A.MATRICULA = B.MATRICULA
GROUP BY A.MATRICULA, B.MATRICULA;

SELECT A.NOME, A.BAIRRO, A.MATRICULA, A.PERCENTUAL_COMISSAO,
COUNT(*) conta FROM tabela_de_vendedores A 
INNER JOIN notas_fiscais B
ON A.MATRICULA = B.MATRICULA
GROUP BY A.NOME
ORDER BY conta ASC;

